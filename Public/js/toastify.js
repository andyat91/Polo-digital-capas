//Toastify suscripcion-------------------------------------------------------------------------------------------------------
function Tsuscripcion(evento) {
  Toastify({
    text: evento,
    duration: 2000,
    destination: "https://example.com",
    newWindow: true,
    close: false,
    gravity: "top",
    position: "center",
    stopOnFocus: true,
    style: {
      background: "var(--opuesto)",
      width: "200px",
      "min-height": "80px",
      opacity: 1,
    },
  }).showToast();
};

